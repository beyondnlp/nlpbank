#ifndef _COLOR_H
#define _COLOR_H 1

/* ansi terminal coloring */
#define CLEAR       "\e[0m"
#define RESET       "\e[0m"
#define BOLD        "\e[1m"
#define DARK        "\e[2m"
#define UNDERLINE   "\e[4m"
#define UNDERSCORE  "\e[4m"
#define BLINK       "\e[5m"
#define REVERSE     "\e[7m"
#define CONCEALED   "\e[8m"
#define GRAY        "\e[1;30m"
#define BLACK       "\e[1;30m"
#define RED         "\e[1;31m"
#define GREEN       "\e[1;32m"
#define YELLOW      "\e[1;33m"
#define BLUE        "\e[1;34m"
#define MAGENTA     "\e[1;35m"
#define CYAN        "\e[1;36m"
#define WHITE       "\e[1;37m"
#define ON_BLACK    "\e[0;40m"
#define ON_RED      "\e[0;41m"
#define ON_GREEN    "\e[0;42m"
#define ON_YELLOW   "\e[0;43m"
#define ON_BLUE     "\e[0;44m"
#define ON_MAGENTA  "\e[0;45m"
#define ON_CYAN     "\e[0;46m"
#define ON_WHITE    "\e[0;47m"

#endif
